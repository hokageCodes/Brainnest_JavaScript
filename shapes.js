// MATHEMATICAL SHAPES
// Area of Triangle using side of lengths is 1/2(side1 + side2 + side 3)
function areaOfTriangle(l1, l2, l3) {
    return 0.5 * (l1 + l2 + l3);
}
console.log(areaOfTriangle(5, 6, 7));

// Circumference of a Circle is 2 * PI * r
function circumferenceOfCircle(PI=3.14, radius=4) {
    return 2 * PI * radius;
}
console.log(circumferenceOfCircle());

// Surface area of a cricle is PI * r * r
function areaOfCrcle(PI=3.14, radius=4) {
    return PI * radius * radius;
}
console.log(areaOfCrcle());


